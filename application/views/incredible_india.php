                        <h4 class="box-title"><strong style="color: #00A8FF;">Amazing destinations in Incredible India</strong></h4>
                        <p>
                            India conjures up an image of billion people rushing through the streets and roads of different cities.
                            Nonetheless, it is also a country with rich culture, monumental history and historical monuments spread across different states.
                            Hot deserts of Rajasthan, freezing lakes of Ladakh, dense jungles of Madhya Pradesh, breathtaking beaches of Goa; the list is enchantingly endless.
                            Onerooftravel takes you through some of the places you may have heard and read about:
                            <br/>
                            <strong>1.	Ladakh</strong><br/>
                            Starting from the very north of India, Jammu and Kashmir, lies Ladakh, the land of passes.
                            It stretches from the Kunlun Mountains to the Himalayas and is inhabited by people of Tibetan descent that reflect in its history and culture.
                            The barren beauty of Ladakh has breathtaking landscapes, pristine lakes nestled among mountains and rejuvenating fresh air.
                            You’ll find Buddhist monasteries that are a sign of Buddhism here.
                            Summer is the best time to travel as the temperatures hit sub-zero during winter.
                            Tourist can reach it by the nearest airport, Leh or through Jammu railway station.
                            However, adventure-loving tourists prefer to cruise by their bikes or travel by road to Leh enjoying the mountains and rugged region along the way.
                            Here, tourists enjoy scenic beauty, trekking, rafting and biking. <br/>
                            <img src="../assets/images/ladakh.jpg" />

                            <br/>
                            <strong>2.	Majuli</strong><br/>
                            Located in the northeast state of Assam, Majuli was formed by the Brahmaputra river.
                            It is the largest river island in the world and has found its place in Guinness Book of World Records.
                            Dating back to 16th century, Majuli is also known for Hindu Vaishnavite monasteries (satras).
                            These places of worship consist of a large prayer hall where devotees can worship Vishnu and Krishna and watch traditional performances such as raaslela.
                            These Satras are indeed a treasure of cultural artifacts.
                            The island happens to be home to around several species of bird and therefore a bird lover’s perfect paradise.

                            <img src="../assets/images/majuli.jpg" />
                            <br/>
                            <strong>3.	Rajasthan</strong><br/>
                            The land of color with scores of forts, museums, national parks palaces and lakes, Rajasthan is located in the north west of India sharing its international border with Pakistan.
                            The capital of Rajasthan, Jaipur is known as the Pink city due to resemblance to red color of the buildings.
                            At the center of Jaipur is the City Palace Complex with beautiful gardens, huge courtyards and museums.
                            From the 18th century lies an astronomical observatory, Jantar Mantar, which is open air with fixed astronomical instruments.<br/>
                            <img src="../assets/images/rajasthan.jpg" />
                            Hawa Mahal, the palace of winds, is a five-storey wall like Redstone building that was used by royal women to enjoy the festivities going on in the streets.
                            Amer Fort, a 16th-century fort and a palace atop a hill, is situated outside the city which features carvings and paintings on the wall.
                            It is witness to many battles fought between the Hindu Rajas and Mughal army.
                            On the way to the fort, you will come across Man Sagar Lake where you can see Jal Mahal, an architectural beauty in the middle of the lake, partly submerged in water.
                            Jaipur is just the starting and there are many more interesting places to explore.
                            Udaipur, founded by Maharana Udai Singh II, is known as a Lake City due to its famous lakes as Jaisamand Lake, the largest artificial lake in Asia.
                            The Lake Palace, a hotel now, is located in the middle of Lake Pichola offering a royal view.
                            Outside of Udaipur, Monsoon Palace is located hilltop and offers panoramic views.
                            Udaipur has some of the most luxurious resorts and hotels.
                            Jaisalmer, a golden city, is near India-Pakistan border and the Thar desert.
                            It’s famous for Jaisalmer fort, also known as Golden Fort.
                            Here, unlike other forts, people live and it houses shops and hotels.
                            In January/February, Desert festival celebrated which happens to the best time to visit and witness folk dances and songs.
                            Havelis, museums, palaces, lakes, forts, temples, a national park and a desert; there is everything in Jaisalmer.
                            <img src="../assets/images/jaipur.jpg" />
                            <br/>
                            <strong>4.	Agra</strong><br/>
                            A historical Mughal city in the largest state of India, Agra is the home of the magnificent Taj Mahal.
                            It needs no introduction to tourists who come here to witness this man-made beauty carved out of white marble.
                            Unsurprisingly, it attracts millions of visitors every year.
                            The Taj Complex is spread across many acres and one can spend an entire day watching Mughal-era architecture.
                            Mughal Garden, The Great Gate and a Mosque are part of it and equally interesting as the Taj.
                            Across the Mughal Garden, lies Red fort where the Mughal Emperor was imprisoned until the last days of his life.
                            This city will surely take you back in history.
                            <img src="../assets/images/taj_mahal.jpg" />
                            <br/>
                            <strong>5.	Goa</strong><br/>
                            A western Indian state, Goa, is famous among foreign tourists.
                            For long, it was a Portuguese colony and got free in 1961, long after India got independence.
                            It’s known for beaches spread along the western coast. Querim, Anjuna, Calangute are some of the beaches worth mentioning.
                            Apparently, the Portuguese have left a lasting impression on its culture, food and music.
                            Famous churches as Church of Our Lady of the Immaculate Conception in Panaji prove it abundantly.
                            The forts dot the coast of Goa and you can see Cabo da Rama Fort and Fort Aguada Jail, Goa’s largest prison, below the Aguada Fort.
                            <img src="../assets/images/goa.jpg" />
                           <br/>
                            <strong>6.	Kerala</strong><br/>
                            Not everything is concentrated in the North of India.
                            Head to south to spend some time in the lap of nature, Kerala, known as ‘God’s Own Country’.
                            It is a coastal state in the south of India known for its backwaters which can be enjoyed in house boat made for tourists who can enjoy tranquility along the palm-bordered Kerala canals.
                            With three adjacent beautiful beaches, Kovalam is a heaven for foreign tourists and there is no dearth of five star hotels.
                            It also has budget cottages and Ayurvedic massage centers in the beach complex. Munnar, a hill station, is renowned for tea plantations on misty hills.
                            If you are a nature and tea lover, it’s a must-see place as it’s home to a tea museum.
                            Periyar National Park is known for its elephants, and if you want to cruise the jungle on elephant-back, you can enjoy elephant rides here.
                            Though at some distance from each other, backwaters of Alleppy, beaches of Kovalam, wildlife of Thekkady and hill station of Munnar are major attractions spread across charismatic Kerala.
                            <img src="../assets/images/kerala.jpg"/>
                            You can spend months in this culturally diverse country exploring its historical cities, monuments and pilgrims.
                            There is so much to see and experience that it will leave you satisfied and full of memories to cherish.


                        </p>
